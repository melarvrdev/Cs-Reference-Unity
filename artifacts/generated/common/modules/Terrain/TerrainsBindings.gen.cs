public static partial class TerrainExtensions
{
    static public void UpdateGIMaterials(this Terrain terrain)
        {
            if (terrain.terrainData == null)
                throw new ArgumentException("Invalid terrainData.");
            UpdateGIMaterialsForTerrain(terrain, new Rect(0, 0, 1, 1));
        }
    
    
    static public void UpdateGIMaterials(this Terrain terrain, int x, int y, int width, int height)
        {
            if (terrain.terrainData == null)
                throw new ArgumentException("Invalid terrainData.");
            float alphamapWidth = terrain.terrainData.alphamapWidth;
            float alphamapHeight = terrain.terrainData.alphamapHeight;
            UpdateGIMaterialsForTerrain(terrain, new Rect(x / alphamapWidth, y / alphamapHeight, width / alphamapWidth, height / alphamapHeight));
        }
    
    
    internal static void UpdateGIMaterialsForTerrain (Terrain terrain, Rect uvBounds) {
        INTERNAL_CALL_UpdateGIMaterialsForTerrain ( terrain, ref uvBounds );
    }

    [UnityEngine.Scripting.GeneratedByOldBindingsGeneratorAttribute] // Temporarily necessary for bindings migration
    [System.Runtime.CompilerServices.MethodImplAttribute((System.Runtime.CompilerServices.MethodImplOptions)0x1000)]
    private extern static void INTERNAL_CALL_UpdateGIMaterialsForTerrain (Terrain terrain, ref Rect uvBounds);
}

